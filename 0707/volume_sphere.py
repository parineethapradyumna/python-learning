# Program to calculate the volume  of a Sphere
#Asking the user to enter the value
r = input("Enter the value of Radius(r): ")
#convert the string to float
r = float(r)
#The formula for volume  of a Sphere
sum = 4/3*3.14*r**3
#print the statement
print(f"Volume  of a Sphere is: {sum} Cm")



