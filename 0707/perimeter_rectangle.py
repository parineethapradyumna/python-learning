#Creating the perimeter of the Rectangle Calculater
#Asking the user to enter the value
l = input("Enter the value of length(L): ")
w = input("Enter the value of Width(W): ")
#Converting the string to float
l=float(l)
w=float(w)
#The formula of perimeter of rectangle
sum = 2*(l+w)
#We have to print the value
print(f"The perimeter of a rectangle is: {sum} Cm ")


