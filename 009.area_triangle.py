# We are creating the area of triangle calculator
# We are asking the user to enter the base and height values
b = input("Enter the value of Base(b): ")
h = input("Enter the value of Height(h): ")
# Converting the string to int
b = int(b)
h = int(h)
# This is the formula of area of triangle
sum = 1/2*b*h
# We have to print the statement
print(f"The value of area of triangle is: {sum} Cm")


####OUTPUT####
