# We are creating the Volume of a Cylinder calculator
# Asking the user to enter the value of radius and height
r = input("Enter the value of Radius(r): ")
h = input("Enter the value of Height(h): ")
#Convert the string to float
r = float(r)
h = float(h)
# This is the formual of Volume of a Cylinder
sum = 3.14*r**2*h
# Print the output
print(f"The value of, Volume of a Cylinder is : {sum} Cm")



