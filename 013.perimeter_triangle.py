# We are creating perimeter of a triangle calculator 
# Asking the user to enter the value of the sides
a = input("Enter the value of side(a): ")
b = input("Enter the value of side(b): ")
c = input("Enter the value of side(c): ")
# Converting the string to float
a = float(a)
b = float(b)
c = float(c)
# This is the formula of perimeter of a triangle
sum = a+b+c
# Print the output
print(f"The value of the perimeter of a triangle is: {sum}")


