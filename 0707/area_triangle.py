#calculate the area of a triangle

#Getting Base and Height Value
b = input("Enter the Base Value: ")
h = input("Enter the Height Value: ")

#Convert input values into integer
b = int(b)
h = int(h)

#Formula for Area of Trinagle
sum = (1/2 *b*h)

# At last we will print the output
print(f"This the value of Area of Triangle: {sum} Cm")

