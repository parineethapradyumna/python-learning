# We are creating the area of square calculator
# We are asking the user to give the value of side
a = input("Enter the value of Side(S): ")
# converting the string into float
a = float(a)
# This is the formula for area of Square
sum = (a**2)
# Now we will print the statement
print(f"This is the value of area of Square: {sum} Cm")


