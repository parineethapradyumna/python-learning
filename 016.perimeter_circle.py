# We are creating the Perimeter of a circle calculator
# Asking the user to enter the value of radius
r = input("Enter the value of Radius(r): ")
# Converting the string to float
r = float(r)
# This is the formual of Perimeter of a circle calculator
sum = 2*3.14*r
# Print the output
print(f"The value of Perimeter of a circle calculator is : {sum} Cm")

