# We are creating perimeter of a square calculator 
# Asking the user to enter the value of a
a = input("Enter the length of side a: ")
# Converting the String to float
a = float(a)
# This is the formula for perimeter of a square
sum= (4*a)
# Print the output
print(f"Perimeter of a square is: {sum} Cm")

