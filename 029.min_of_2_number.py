# Asking the user to enter the any two numbers in a and b 
a = int(input("Enter a number(a): "))
b = int(input("Enter a numnber(b): "))
# if a is less than b
if a < b:
# Then the minimum number will be "a" Value
    print(f"min = {a}")
# Or else it it show "b" value
else:
    print(f"min = {b}")

