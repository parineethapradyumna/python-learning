# Program to calculate the surface of a Sphere
#Asking the user to enter the value
r = input("Enter the value of Radius(r): ")
#convert the string to float
r = float(r)
#the formula for surface of a Sphere
sum = 4*3.14*r**2
#print the statement
print(f"Surface of a Sphere is: {sum} Cm")


