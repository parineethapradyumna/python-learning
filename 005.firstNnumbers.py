# we are creating first N Natural Number Calculator
# asking the user to enter a number
n = input("Enter a number: ")
# converting the string to int
n = int(n)
# this is the formula of first N Natural Number 
sum = n*(n+1)/2
# we have to print the statement
print(f"The value of Frist Natural Number is: {sum}")

