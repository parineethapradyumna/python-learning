# We are creating perimeter of a Rectangle calculator
# Asking the suer to enter the width and length 
w = input("Enter the value of width(w): ")
l = input("Enter the value of length(l): ")
# Converting the string to float
w = float(w)
l = float(l)
# This is the formula of perimeter of Rectangle
sum = 2*(w+l)
# Print the output
print(f"This value of perimeter of rectangle is :{sum} Cm ") 

